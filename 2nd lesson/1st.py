# Операторы членства
 # in - возвращает True, если значение есть в цепочке(если in не находит элемент, кототрый мы ищем, то получаем результат False, если находит, то True)
 # not in - возвращает True, если значения в цепочке нет(если мы думаем,что элемента в цепоче нет, но not in находит его, то мы получаем результат False, если не находит, то True)
 #
 # Например:
 # print('arckline' in 'website=arckline') - True - потому что элемент 'arckline' есть с обеих сторон
 # print('arckline' in 'qte1234567') - False - потому что элемент 'arckline' есть только с 1й стороны
 # print('arckline' not in 'qte1234567') - True - потому что элемент 'arckline' отсутствует с другой стороны 
 # print('arckline'  not in 'website=arckline') - False - потому что элемент 'arckline' есть с обеих сторон #