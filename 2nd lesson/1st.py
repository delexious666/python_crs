# Операторы сравнения:
 # > - больше
 # < - меньше
 # == - равно 
 # != - не равно 
 # >= - больше или равно 
 # <= - меньше или равно
 #  
 # Например:
 # print(1 == 2)
 # print(1 > 2)
 # print(1 < 2)
 # print(1 != 2)
 # print(1 >= 2)
 # print(1 <= 2) #

# Логические операторы:
 # or - логисеское "или"
 # and - логическое "и"
 # not - логическое "отрицание"
 #
 # Например:
 # print(1+1 == 2 or 1*1 == 2)
 # print(1+1 == 2 and 1*1 == 2)
 # print(1+1 == 2 and  1*1 == 1)
 # print(1+1 == 2 and not 1*1 == 1)
 # print(1+1 == 2 and not 1*1 == 2) #

# Арифметические операторы:
 # + - сложение
 # - вычитание
 # * - умножение
 # / - деление
 # ** - возведение в степень
 # // - целочисленное деление (int)
 # % - деление по модулю (возврат остатка от деления)
 #
 # Например:
 # print(10 + 3)
 # print(10 - 3)
 # print(10 * 3)
 # print(10 / 3)
 # print(10 // 3)
 # print(11 ** 2)
 # print(3 ** 2)
 # print(10 % 3) #

